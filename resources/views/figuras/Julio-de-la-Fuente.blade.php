                        <h4>Estudios en Antropología</h4>
                        <p  style="text-align: justify;">
                        En 1939 ingresó al Instituto de Antropología, descubierto por Alfonso Caso cuando presentó sus estudios pioneros en las sesiones del Congreso de Americanistas que se celebraron en México. Fue ayudante de Bronislaw Malinowski (fundador de la antropología funcional británica) para realizar una investigación sobre el mercado de Oaxaca. Su contribución fue tan destacada que Malinowski, al redactar la versión final del informe incluyó su nombre. Recibió cursos de Malinowski y Murdock en la Universidad de Yale en 1941 y de Redfield y Tax en la Universidad de Chicago, que lo colocaron en la antropología mexicana como un analista social mayor. 
                        </p>  
