                    <h4>Biografía</h4>

                        <p style="text-align: justify;">
                        Nació en San Lorenzo de Cerralvo, hoy Yanga en 1905 y murió en Ciudad de México el 30 de junio de 1970. En 1923 ingresó a la Facultad de Ciencias Químicas de la Universidad Nacional. Antes de concluir la carrera abandonó los estudios y se empleó en una factoría; el contacto con obreros de filiación progresista lo lanzó a la aventura y en 1928 se estableció en Nueva York como militante de una liga antiimperialista. Trabajó como dibujante, pintor y traductor y, durante la depresión, como platero. La gran crisis capitalista lo hizo volver a su patria. Gabriel Lucio lo designó maestro rural y quedó comisionado en la Dirección General de Educación Popular, dedicado a sus labores de dibujante: ilustró los primeros libros gratuitos de lectura para la enseñanza primaria, escritos por el propio profesor Lucio y el profesor Manuel Mejía, en la colección Simiente. Fue uno de los organizadores del Grupo Noviembre.                        </p>
                        <br>
                        <p style="text-align: justify;">
                        Hizo varios trabajos para Miguel Bustos Cerecedo: las portadas de dos de sus libros y le ilustró una novela corta infantil; se convirtió en el artista gráfico del grupo y así sus dibujos aparecieron en la revista Ruta y en Frente a Frente (1936-39), órgano de la Liga de Escritores y Artistas Revolucionarios (LEAR) en la que fungió como secretario general. Ilustró cuentos infantiles de Gabriel Lucio (1933). Colaboró en la Secretaria y Subsecretaría de Educación Pública y en las actividades sociales de la LEAR; posteriormente se dedicó a la fotografía y expuso su obra. 
                    </p>
                       
