                        En 1946 fue profesor visitante de la Universidad de Fisk y desempeñó, desde entonces, plazas docentes o administrativas en la Escuela Superior de Medicina Rural, en el Instituto de Alfabetización en Lenguas Indígenas del Valle del Mezquital y a partir de 1951, en el INI, donde ocupó el cargo de jefe de la Comisión Técnica Consultiva. Fue subdirector de Asuntos Indígenas de la SEP. El INI publicó el libro: Pensamiento antropológico e indigenista de Julio Antonio de la Fuente (1980).
